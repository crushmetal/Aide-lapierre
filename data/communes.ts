export const getCommuneEpci = (name: string): string | undefined => {
  const normalizedInput = name.toLowerCase().trim();
  const found = COMMUNES_DB.find(c => c.nom.toLowerCase() === normalizedInput);
  return found?.epci;
};

export const searchCommunes = (query: string): string[] => {
  if (query.length < 2) return [];
  const normalizedQuery = query.toLowerCase();
  
  // Amélioration de la recherche pour inclure les accents et éviter les doublons
  const matches = COMMUNES_DB
    .filter(c => c.nom.toLowerCase().includes(normalizedQuery))
    .map(c => c.nom);
    
  // Dédoublonnage et limitation
  return [...new Set(matches)].slice(0, 10);
};
